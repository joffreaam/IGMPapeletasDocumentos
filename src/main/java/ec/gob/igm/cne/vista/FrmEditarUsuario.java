    public FrmEditarUsuario(TUsuario usuario) {
        this.usuarioActual = usuario;
        initComponents();
        this.setTitle("Editar de Usuario");
        this.setLocationRelativeTo(null);
        this.setResizable(false);

        TPerfilDAO tpdao = new TPerfilDAO();
        List<TPerfil> perfilList = tpdao.listarTodos();
        String[] arrayPerfil = new String[perfilList.size()];
        for (int i = 0; i < arrayPerfil.length; i++) {
            arrayPerfil[i] = perfilList.get(i).toString();
        }
        cmbPerfil.setModel(new DefaultComboBoxModel(arrayPerfil));

        TDignidadDAO tddao = new TDignidadDAO();
        List<TDignidad> dignidadList = tddao.listarTodas();
        String[] arrayDignidad = new String[dignidadList.size()];
        for (int i = 0; i < arrayDignidad.length; i++) {
            arrayDignidad[i] = dignidadList.get(i).toString();
        }
        cmbDignidad.setModel(new DefaultComboBoxModel(arrayDignidad));

        TBodegaDAO tbdao = new TBodegaDAO();
        List<TBodega> bodegaList = tbdao.listarTodas();
        String[] arrayBodega = new String[bodegaList.size()];
        for (int i = 0; i < arrayBodega.length; i++) {
            arrayBodega[i] = bodegaList.get(i).toString();
        }
        cmbBodega.setModel(new DefaultComboBoxModel(arrayBodega));

        if (usuarioActual != null) {
            txtNombre.setText(usuarioActual.getNombreCompleto());
            txtUsuario.setText(usuarioActual.getUsuario());
            txtPassword.setText(usuarioActual.getPassword());
        }
    }

