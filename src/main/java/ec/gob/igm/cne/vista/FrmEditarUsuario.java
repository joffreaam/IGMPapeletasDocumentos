    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int idUsuario = usuarioActual.getIdUsuario().intValue();
        String nombre = txtNombre.getText();
        String usuario = txtUsuario.getText();
        String plainPassword = String.valueOf(txtPassword.getPassword());

        if (nombre.length() > 0 && usuario.length() > 0 && plainPassword.length() > 0) {
            
            String password;
            if (plainPassword.compareTo(usuarioActual.getPassword()) == 0) {
                /*
                Si el usuario no ha modificado el password significa que el
                password que se va a guardar ya está codificado, por lo que se
                debe guardar como está (sin codificar)
                */
                password = plainPassword;
            } else {
                /*
                Si el usuario modificó el password, entonces el nuevo password
                debe ser codificado antes de guardarse                
                */
                password = PasswordHash.hash(plainPassword);
            }
            String perfil = ((String) cmbPerfil.getSelectedItem()).split("-")[1];
            String[] idDgnidad_dignidad = ((String) cmbDignidad.getSelectedItem()).split("-");
            int idDgnidad = Integer.valueOf(idDgnidad_dignidad[0]);
            String dignidad = idDgnidad_dignidad[1];
            int bodega = Integer.valueOf(((String) cmbBodega.getSelectedItem()).split("-")[0]);

            String msj = usuarioDAO.actualizarUsuario(idUsuario, usuario, password, nombre, perfil, dignidad, BigInteger.valueOf(bodega), (short) idDgnidad);
            JOptionPane.showMessageDialog(rootPane, msj);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Por favor escriba un Nombre, un Usuario y una Contraseña", "Datos faltantes", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

